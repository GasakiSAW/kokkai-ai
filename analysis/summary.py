def summarize(text):

    patterns = [

        (
            ["予算", "物価", "減税", "賃上げ", "財政"],
            "物価高対策や経済政策について発言。"
        ),

        (
            ["年金", "介護", "生活保護", "障害者"],
            "福祉や社会保障制度について発言。"
        ),

        (
            ["医療", "病院", "看護", "診療"],
            "医療制度や地域医療について発言。"
        ),

        (
            ["教育", "学校", "大学", "教師", "生徒"],
            "教育政策や学校運営について発言。"
        ),

        (
            ["子育て", "保育", "少子化", "育児"],
            "子育て支援や少子化対策について発言。"
        ),

        (
            ["農業", "農地", "農村", "食料", "米"],
            "農業政策や食料問題について発言。"
        ),

        (
            ["防衛", "自衛隊", "安全保障", "ミサイル"],
            "防衛政策や安全保障について発言。"
        ),

        (
            ["外交", "国際", "中国", "アメリカ", "韓国", "ロシア"],
            "外交問題や国際関係について発言。"
        ),

        (
            ["環境", "温暖化", "脱炭素", "CO2"],
            "環境政策や脱炭素について発言。"
        ),

        (
            ["災害", "地震", "防災", "避難"],
            "災害対策や防災について発言。"
        ),

        (
            ["雇用", "労働", "最低賃金", "働き方"],
            "雇用や労働環境について発言。"
        ),

        (
            ["AI", "DX", "デジタル", "マイナンバー"],
            "デジタル政策や行政DXについて発言。"
        )
    ]

    matched = []

    for words, sentence in patterns:

        for word in words:

            if word in text:
                matched.append(sentence)
                break

    if matched:
        return " ".join(matched[:2])

    return "国政に関する課題について発言。"
