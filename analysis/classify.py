def classify(text):

    categories = {

        "経済": [
            "予算","物価","減税","賃上げ",
            "経済","税","財政","金融"
        ],

        "福祉": [
            "生活保護","年金","介護",
            "障害者","福祉"
        ],

        "医療": [
            "医療","病院","診療",
            "看護","保険"
        ],

        "教育": [
            "教育","学校","大学",
            "教師","児童","生徒"
        ],

        "子育て": [
            "子育て","保育",
            "少子化","育児"
        ],

        "農業": [
            "農業","農地",
            "農村","米","食料"
        ],

        "防衛": [
            "防衛","自衛隊",
            "安全保障","ミサイル"
        ],

        "外交": [
            "外交","国際",
            "中国","アメリカ",
            "韓国","ロシア"
        ],

        "環境": [
            "環境","温暖化",
            "脱炭素","CO2"
        ],

        "エネルギー": [
            "原発","原子力",
            "電力","再生可能エネルギー"
        ],

        "地方創生": [
            "地方創生",
            "自治体",
            "地域活性化"
        ],

        "災害・防災": [
            "災害","地震",
            "防災","避難"
        ],

        "労働": [
            "雇用","労働",
            "最低賃金","働き方"
        ],

        "デジタル": [
            "AI","DX",
            "デジタル",
            "マイナンバー"
        ],

        "憲法・政治": [
            "憲法","選挙",
            "政治","国会"
        ]
    }

    scores = {}

    for category, words in categories.items():

        scores[category] = 0

        for word in words:

            if word in text:
                scores[category] += 1

    best_category = max(
        scores,
        key=scores.get
    )

    if scores[best_category] == 0:
        return "その他"

    return best_category
